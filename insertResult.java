    public String getGrade(String a,String b){
        int aa = Integer.valueOf(a);
        int bb = Integer.valueOf(b);
        int n = aa+bb;
        if(n>90) return "A+";
        if(n>80) return "A";
        if(n>70) return "B";
        if(n>60) return "C";
        if(n>50) return "D";
        if(n>40) return "E";
        return "F";
    }
